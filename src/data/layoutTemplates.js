// Helper function to get layouts by panel count
export const getLayoutsByPanelCount = (panelCount) => {
    return layoutTemplates.filter(layout => layout.panelCount === panelCount);
};

// Helper function to get a layout by ID
export const getLayoutById = (id) => {
    return layoutTemplates.find(layout => layout.id === id);
};

// Get all unique panel counts available
export const getAvailablePanelCounts = () => {
    return [...new Set(layoutTemplates.map(l => l.panelCount))].sort((a, b) => a - b);
};

// Convert polygon points to CSS clip-path polygon string
export const toClipPath = (points) => {
    return `polygon(${points.map(([x, y]) => `${x}% ${y}%`).join(', ')})`;
};

// Convert polygon points to SVG path
export const toSvgPath = (points, width = 100, height = 100) => {
    const scaled = points.map(([x, y]) => [x * width / 100, y * height / 100]);
    return `M ${scaled.map(([x, y]) => `${x},${y}`).join(' L ')} Z`;
};

export default layoutTemplates;
